package utils;

public class Color {
  public int red;
  public int green;
  public int blue;

  public Color(int red, int green, int blue) {
    this.red = red;
    this.green = green;
    this.blue = blue;
  }

  public Color(int grey) {
    this.red = grey;
    this.green = grey;
    this.blue = grey;
  }
}
